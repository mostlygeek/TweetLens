            <h2>What is TweetLens?</h2>
            <p><strong>TweetLens is a rich web based Twitter interface. Downloads unnecessary.</strong></p>
            <p>Some of the features that make TweetLens awesome:<p>
            <ol>
                <li>Keyboard based navigation</li>
                <li>Background status updates</li> 
                <li>Mark as Read</li>
                <li>Hide read tweets</li>
                <li>One key retweeting</li>
                <li>One key replying</li>
                <li>Mouse navigation</li>
                <li>Integrated Twitter Search</li>
                <li>Saved Searches</li>
                <li>TwitPic preview integration</li>
                <li>Automatic URL expansion</li>
                <li>Inline Reply tree display</li>
                <li>Inline Replies</li>
                <li>... and lots more coming</li> 
            </ol>
            
